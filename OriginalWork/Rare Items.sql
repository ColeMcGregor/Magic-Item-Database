SELECT rc1.name,
       rc1.price
FROM   RareCons AS rc1
	WHERE  rc1.itemID = (
	   SELECT MIN(rc2.itemID)
	   FROM   RareCons AS rc2
	   WHERE  rc2.name = rc1.name
	)
ORDER BY rc1.name ASC;


CREATE TABLE "RareItem" (
	"itemID"	INTEGER UNIQUE,
	"name"	TEXT NOT NULL,
	"price"	INTEGER,
	"attune"	TEXT NOT NULL,
	PRIMARY KEY("itemID" AUTOINCREMENT),
	CHECK("attune" IN ('Y', 'N'))
);